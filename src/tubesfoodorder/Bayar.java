    private void txtbayarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtbayarKeyPressed
        int cash = Integer.parseInt(txtbayar.getText());
       int total = Integer.parseInt(txttotal.getText());
        if (evt.getKeyCode() == KeyEvent.VK_ENTER){
            if (cash < total){
                JOptionPane.showMessageDialog(null, "Kurang cuan");
            } else{
                kembalian = cash - total;
               txtkembalian.setText(""+kembalian);
               btnbayar.setEnabled(true);
            }
           
       }
        
    }//GEN-LAST:event_txtbayarKeyPressed

