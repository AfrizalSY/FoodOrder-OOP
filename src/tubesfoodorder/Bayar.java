    public void connection(){
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            cn = DriverManager.getConnection("jdbc:mysql://localhost:3307/janjikita", "root", "");
        } catch(Exception ex) {
            JOptionPane.showMessageDialog(null, "Koneksi close");
        }
    }
    
    private void btnbayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbayarActionPerformed
        connection();
        
        try{
                    File struk = new File("Struck.txt");
                try {
                    FileWriter tulis = new FileWriter("Struck.txt");

                    tulis.write("========================== Janji Kita ==========================\n======================== Coffe & Toast =========================\n\n");
                    
                    PreparedStatement ps = cn.prepareStatement("select * from keranjang");
                    ResultSet rs = ps.executeQuery();
                    tulis.write("Produk\t\t\t\tKuantitas\tSub Total \n");
                    tulis.write("----------------------------------------------------------------\n");
                    while (rs.next()){
                        tulis.write(rs.getString(2)+ "\t\t" + rs.getInt(3)+"\t\t" +rs.getInt(4)+"\n");
                    }
                    tulis.write("----------------------------------------------------------------\n");
                    tulis.write("Grand Total\t\t\t\t\t" +txttotal.getText()+"\n");
                    tulis.write("Bayar\t\t\t\t\t\t" +txtbayar.getText()+"\n");
                    tulis.write("Kembalian\t\t\t\t\t" +kembalian+ "\n\n");
                    tulis.write("======================= Terima Kasih <3 ======================\n" +
"========= Semoga kamu cepat bertemu dengan jodohmu ===========");
                    tulis.close();
                }catch (IOException e) {
                    System.out.println("error");
                    e.printStackTrace();
                }
                PreparedStatement ps = cn.prepareStatement("delete from keranjang");
                ps.execute();
                cn.close();
                JOptionPane.showMessageDialog(null,"Lunas, terimakasih (: ");
                this.dispose();
                new Main().setVisible(true);
        } catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        
    }//GEN-LAST:event_btnbayarActionPerformed

