    private void btngbrActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btngbrActionPerformed
        Image images;
        
        JFileChooser gbr = new JFileChooser(System.getProperty("user.home"));
        gbr.setFileFilter(new FileNameExtensionFilter("jpg|png", "jpg", "png"));
        gbr.setFileSelectionMode(JFileChooser.FILES_ONLY);
        if(gbr.showOpenDialog(this)==JFileChooser.APPROVE_OPTION){
            File file = gbr.getSelectedFile();
            try{
                images = ImageIO.read(file);
                
                ImageIcon icon = new ImageIcon(resize(pangbr.getWidth(), pangbr.getHeight(), images));
                pangbr.setIcon(icon);
                path = file.getAbsolutePath();
                txtgbr.setText(file.getName());
            } catch (Exception ex){
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_btngbrActionPerformed

    private Image resize(int w, int h, Image img){
        BufferedImage bufimg = new BufferedImage(w,h, BufferedImage.TYPE_INT_RGB);
        Graphics2D dimensi = bufimg.createGraphics();
        dimensi.setRenderingHint(RenderingHints.KEY_INTERPOLATION, RenderingHints.VALUE_INTERPOLATION_BILINEAR);
        dimensi.drawImage(img, 0,0,w,h, null);
        dimensi.dispose();
        return bufimg;
    }
