    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        connection();
        String prod = txtproduk.getText();
        String harga = txtharga.getText();
//        String gambar = txtgbr.getText();
        
        try{
            PreparedStatement ps = cn.prepareStatement("insert into produk (nama_produk, harga, gambar) values (?,?,?)");
            ps.setString(1, prod);
            ps.setString(2, harga);
            if(!txtgbr.getText().equals("")){
                ByteArrayOutputStream output = new ByteArrayOutputStream();
                File img = new File(path);
                FileInputStream input = new FileInputStream(img);
                try{
                    byte[] buf = new byte[1024];
                    for (int readbyte;(readbyte=input.read(buf))!=-1;){
                        output.write(buf,0,readbyte);
                    }
                    bitarray = output.toByteArray();
                    setgambar(bitarray);
                    pangbr.setIcon(null);
                } catch (Exception ex){
                }
            ps.setBytes(3, getgambar());
            }
            ps.execute();
            cn.close();
            JOptionPane.showMessageDialog(null, "Berhasil Disimpan");
            txtid.setText("-");
            txtproduk.setText("");
            txtharga.setText("");
        } catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnsaveActionPerformed

