    public void ambilgambar(){
        try{
            int id = Integer.parseInt(txtid.getText());
            byte[] img = getgambar(id);
            BufferedImage file = ImageIO.read(new ByteArrayInputStream(img));
            ImageIcon icon = new ImageIcon(resize(pangbr.getWidth(), pangbr.getHeight(), file));
            pangbr.setIcon(icon);
        } catch (Exception ex){
            
        }
    }
    public void loadrecord(JTable tabelproduk){
        try{
                PreparedStatement ps = cn.prepareStatement("select * from produk");
                ResultSet rs = ps.executeQuery();
                DefaultTableModel model = (DefaultTableModel)tabelproduk.getModel();
                model.setRowCount(0);
                Object[] row;
                while (rs.next()){
                    row = new Object[3];
                    row[0] = rs.getInt(1);
                    row[1] = rs.getString(2);
                    row[2] = rs.getInt(3);
                    model.addRow(row);
                }
                ps.execute();
//                ambilgambar();
        } catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }
